function weightedPick(items){
  const total=items.reduce((s,i)=>s+Math.max(0,i.w),0); if(total<=0)return items[0]?.id||null;
  let r=Math.random()*total; for(const it of items){r-=Math.max(0,it.w); if(r<=0)return it.id;} return items[0]?.id||null;
}

function aiAtk(){
  const side=G.D.as,carrier=G.D.carrier,cp=PP[side][G.ck];
  const sp=carrier?carrier.spirit||1500:1500;
  const prog=cp?progressFor(side,cp):0;
  // pressure: 0 = completely free, 1 = defender right on top
  const defDist=cp?nearestDefenderDistance(side,cp):W*.16;
  const pressure=clamp(1-defDist/(W*.18),0,1);
  const space=1-pressure; // how much room the carrier has
  const canAfford=id=>sp>=(ATK_ACTIONS[id]||{}).cost||0;
  const options=[];

  // PASS — preferred when under pressure OR a clearly better-positioned teammate exists.
  // Reduce the flat low-progress bonus so CPU doesn't ALWAYS pass from deep.
  const bestPass=bestTeammateFor(side,G.ck,'pass');
  if(bestPass){
    // Base pass weight: pressure-reactive but not dominant at low prog
    const passW=1.8+pressure*2.4+prog*0.6;
    options.push({id:'pass',w:passW});
  }

  // DRIBBLE — reward when there's space (high `space`), regardless of field position.
  // Scale also with carrier's SPD stat relative to 75 baseline.
  if(canAfford('dribble')){
    const spdBonus=carrier?((carrier.spd||70)-70)/30:0; // -1..+1
    const dribbleW=1.4+space*2.8+spdBonus*1.2+prog*0.8;
    options.push({id:'dribble',w:dribbleW});
  }

  // SHOOT — from attacking third, and especially on a direct shot attempt.
  if((G.D.isShot||prog>.55)&&canAfford('shoot')){
    const shootW=(G.D.isShot?5.0:0.8)+prog*4.5-pressure*1.2;
    options.push({id:'shoot',w:shootW});
  }

  // CHIP — good specifically when GK is close / ball is very central.
  if((G.D.isShot||prog>.65)&&canAfford('chip')){
    const chipW=(G.D.isShot?2.8:0.5)+prog*2.2-pressure*0.8;
    options.push({id:'chip',w:chipW});
  }

  // SPECIAL — open the window earlier (prog > 0.55 on shot duels) so it actually fires.
  const spec=getSpecial(carrier);
  if(spec&&canAfford('special')&&(G.D.isShot||prog>.58)){
    const specW=(G.D.isShot?6.0:1.6)+prog*5.0-pressure*0.6;
    options.push({id:'special',w:specW});
  }

  // ONE-TWO — wall pass, good under pressure in midfield
  const ot=bestTeammateFor(side,G.ck,'one-two');
  if(ot&&canAfford('one-two')&&!G.D.isShot){
    const oneTwoW=1.6+pressure*2.2+prog*1.0;
    options.push({id:'one-two',w:oneTwoW});
  }

  G.D.ak=weightedPick(options.filter(o=>o.w>0))||'pass';
  if(G.D.ak==='pass')G.D.pk=bestPass;
  if(G.D.ak==='one-two')G.D.pk=ot||bestPass;
  if(G.D.as==='a'&&G.D.ds==='a')tryRes();
}

function aiDef(){
  const def=G.D.def,ds=G.D.ds,as=G.D.as,cp=PP[as][G.ck];
  const prog=cp?progressFor(as,cp):.5;
  const centrality=cp?1-Math.abs(cp.y/H-.5):.5;
  const defIsGK=def&&def.pos==='GK';
  const ak=G.D.ak; // attacker's choice — may be set if aiAtk ran first

  if(G.D.isShot&&defIsGK){
    // GK should punch on high/wide shots, save on central shots
    const punchBias=prog>.88?(1.8):((1-centrality)*1.4);
    const opts=[{id:'save',w:3.0+centrality*1.2},{id:'punch',w:1.2+punchBias}];
    G.D.defA=weightedPick(opts);
  } else {
    // Smart counter-pick: if we know the attacker's move, weight heavily against it.
    // If we don't know (ak===null, AI vs AI race), use positional heuristics.
    let tackleW=2.0, interceptW=1.8, blockW=1.6;

    if(ak==='pass'||ak==='one-two'){
      // Intercept counters pass/one-two in RPS
      interceptW+=3.0;
      tackleW-=0.4;
    } else if(ak==='dribble'){
      // Tackle counters dribble in RPS
      tackleW+=3.0;
      interceptW-=0.6;
    } else if(ak==='shoot'||ak==='special'){
      // Block counters shoot/special in RPS
      blockW+=3.2;
      tackleW-=0.2;
    } else if(ak==='chip'){
      // Block partially counters chip too
      blockW+=2.0;
    } else {
      // No info — use position/situation heuristics
      tackleW+=(prog<.55?1.4:0.2);
      interceptW+=(prog>.40?1.0:0.2);
      blockW+=(prog>.65?1.4:0.4)+centrality*0.8;
    }

    // Situational adjustments
    tackleW+=(centrality<.35?0.8:0);  // wide areas = more tackle attempts
    blockW+=centrality*1.0;           // central = more blocks
    interceptW+=(prog>.45?0.6:0);     // further up = intercept more likely

    // Spirit-based adjustment: depleted defender leans tackle (low cost)
    if(def){
      const sp=def.spirit||1500,maxSp=1500;
      if(sp<maxSp*.4){tackleW+=0.8;blockW-=0.4;interceptW-=0.2;}
    }

    const opts=[{id:'tackle',w:tackleW},{id:'intercept',w:interceptW},{id:'block',w:blockW}];
    G.D.defA=weightedPick(opts.filter(o=>o.w>0));
  }
  if(G.D.as==='a'&&G.D.ds==='a')tryRes();
}

function tryRes(){
  // Don't auto-resolve if human still needs to choose attack or defence
  if(G.D.as==='h' && !G.D.ak) return;
  if(G.D.ds==='h' && !G.D.defA) return;
  if(G.D.ak && G.D.defA) resDuel();
}
function confirmDuel(){
  if(!G.D.ak && G.D.as==='a') aiAtk();
  if(!G.D.defA && G.D.ds==='a') aiDef();
  // Only resolve once both sides have a choice — never auto-fill human
  if(G.D.ak && G.D.defA) resDuel();
}

