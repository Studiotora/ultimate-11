    closeDuel();
    document.getElementById('rbadge').textContent=outcomeText[outcome];
    document.getElementById('rbadge').style.color=outcome==='goal'?'#f0c040':'#4db8ff';
    document.getElementById('rdet').textContent=outcomeDetail[outcome];
