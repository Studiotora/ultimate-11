function moveOffBall(s,ds){
  const cp=PP[s][G.ck]; if(!cp)return;
  const dir=dirFor(s);
  const carrierProg=progressFor(s,cp);
  const dangerProg=carrierProg;
  const threatLevel=clamp((dangerProg-.30)/.45,0,1);

  // ── PHILOSOPHY ────────────────────────────────────────────────
  // Most players barely move — only players with an ACTIVE ROLE
  // move with purpose. Everyone else holds their formation position.
  // This creates the CT tactical stillness broken by purposeful runs.
  //
  // Movement speed is capped LOW. Players should glide into position
  // over 1-2 seconds, not snap there in a frame.
  // ─────────────────────────────────────────────────────────────
  const DRIFT_SPEED   = W * 0.00025; // formation hold — barely perceptible
  const ROLE_SPEED    = W * 0.00050; // active defending role
  const SUPPORT_SPEED = W * 0.00040; // runners

  function glide(cur, tx, ty, spd){
    const dx=tx-cur.x, dy=ty-cur.y;
    const d=Math.hypot(dx,dy);
    if(d<1) return;
    // Never move more than spd per frame regardless of distance
    const step=Math.min(spd, d);
    cur.x += (dx/d)*step;
    cur.y += (dy/d)*step;
  }

  // ── ATTACKING TEAM ────────────────────────────────────────────
  // Compute a "safe forward X" — runners never go behind the halfway line
  // when attacking, never into own box when defending
  const ownGoalX = ownGoalXFor(s);
  const attGoalX = goalXFor(s);
  // Floor: runners must stay at least 20% of pitch ahead of own goal
  const runnerFloor = dir>0 ? clamp(cp.x, W*.20, W*.90) : clamp(cp.x, W*.10, W*.80);

  Object.keys(sq(s)).forEach(k=>{
    if(!sq(s)[k]||k===G.ck||!PP[s][k])return;
    const cur=PP[s][k];
    const p=fp(k,s==='h'?'home':'away',G.half);

    // GK: hold formation tightly
    if(k==='GK'){glide(cur,p.x*W,p.y*H,DRIFT_SPEED);return;}

    // Cooling down: drift back to formation
    if(ocd(s,k)){glide(cur,p.x*W,p.y*H,DRIFT_SPEED*0.5);return;}

    if(k===ROLES.runner1){
      // Runner 1: advance toward attacking third — stable X, not carrier-relative (causes shuffle)
      const tx=clamp(attGoalX*(dir>0?.72:.28), W*.10, W*.92);
      const ty=lerp(p.y*H,cp.y,.15);
      glide(cur,tx,ty,SUPPORT_SPEED);
    }else if(k===ROLES.runner2){
      // Runner 2: slightly deeper wide option — also stable X
      const tx=clamp(attGoalX*(dir>0?.60:.40), W*.10, W*.88);
      const wide=(p.y<.5?Math.max(p.y-.06,.08):Math.min(p.y+.06,.92))*H;
      glide(cur,tx,lerp(wide,cp.y,.06),SUPPORT_SPEED*0.85);
    }else{
      // Everyone else: hold formation — use formation X not carrier-relative X
      // This prevents the whole team collapsing when carrier is a defender
      let tx=p.x*W, ty=p.y*H;
      if(zo(k)==='mid'){
        // Midfielders nudge forward only when ball is already in midfield+
        // Never collapse toward own goal just because carrier is a CB
        const safeProg=clamp(carrierProg, 0.30, 0.85);
        tx=(dir>0?safeProg-.08:1-safeProg+.08)*W;
        tx=clamp(tx, W*.15, W*.85);
        ty=p.y*H;
      }else if(zo(k)==='def'){
        // Defenders: hold their line, don't collapse toward goal
        const safeProg=clamp(carrierProg, 0.10, 0.60);
        tx=(dir>0?safeProg-.25:1-safeProg+.25)*W;
        tx=clamp(tx, W*.06, W*.55); // never past halfway when defending
        ty=p.y*H;
      }
      glide(cur,tx,ty,DRIFT_SPEED);
    }
  });

  // ── DEFENDING TEAM ────────────────────────────────────────────
  // Build marker map once per tick
  const assignedDefs=new Set([ROLES.engager,ROLES.cover,ROLES.blocker]);
  const freeAtk=validOutfieldKeys(s).filter(k=>k!==G.ck);
  const markerMap={};
  Object.keys(sq(ds)).forEach(k=>{
    if(!sq(ds)[k]||assignedDefs.has(k)||k==='GK'||!PP[ds][k])return;
    let bestA=null,bestD=Infinity;
    freeAtk.forEach(ak=>{
      if(!PP[s][ak])return;
      const already=Object.values(markerMap).includes(ak);
      const d=dist(PP[ds][k],PP[s][ak])*(already?2:1);
      if(d<bestD){bestD=d;bestA=ak;}
    });
    if(bestA)markerMap[k]=bestA;
  });

  Object.keys(sq(ds)).forEach(k=>{
    if(!sq(ds)[k]||!PP[ds][k])return;
    if(k===ROLES.engager)return; // engager handled in tick()
    const cur=PP[ds][k];
    const p=fp(k,ds==='h'?'home':'away',G.half);

    if(k==='GK'){
      // GK shifts slightly toward ball lane under threat
      const gkX=p.x*W + (dir>0?-1:1)*threatLevel*W*.04;
      glide(cur,gkX,p.y*H,DRIFT_SPEED);return;
    }
    if(ocd(ds,k)){glide(cur,p.x*W,p.y*H,DRIFT_SPEED*0.5);return;}

    if(k===ROLES.cover){
      // Cover: hold line between carrier and own goal
      const goalX=ownGoalXFor(ds);
      const tx=clamp(lerp(cp.x,goalX,0.40),W*.04,W*.96);
      const ty=lerp(p.y*H,cp.y,0.28);
      glide(cur,tx,ty,ROLE_SPEED);
    }else if(k===ROLES.blocker){
      // Blocker: guard central shooting corridor
      const goalX=ownGoalXFor(ds);
      const tx=clamp(lerp(cp.x,goalX,0.62),W*.04,W*.96);
      const ty=lerp(H*.5,cp.y,0.20);
      glide(cur,tx,ty,ROLE_SPEED*0.85);
    }else if(markerMap[k]){
      // Marker: stay goalside of assigned attacker — KEY TACTIC
      const tgt=PP[s][markerMap[k]];
      if(tgt){
        const goalX=ownGoalXFor(ds);
        const tx=lerp(tgt.x,goalX,0.16);
        const ty=lerp(tgt.y,H*.5,0.10);
        glide(cur,tx,ty,ROLE_SPEED*0.80);
      }else{
        glide(cur,p.x*W,p.y*H,DRIFT_SPEED);
      }
    }else{
      // No role: hold formation with zone compression
      // Clamp so defenders never drift into own box area
      let tx=p.x*W, ty=p.y*H;
      if(zo(k)==='mid'){
        const base=clamp(dangerProg-.08,.22,.70);
        const shifted=clamp(base-threatLevel*.15,.18,.68);
        tx=clamp((dir>0?1-shifted:shifted)*W, W*.12, W*.88);
        ty=p.y*H;
      }else if(zo(k)==='def'){
        const base=clamp(dangerProg-.26,.10,.42);
        const shifted=clamp(base-threatLevel*.10,.08,.38);
        // Hard floor: defending line never closer than 12% to own goal line
        const defFloor = dir>0 ? W*.12 : W*.06;
        const defCeil  = dir>0 ? W*.58 : W*.88;
        tx=clamp((dir>0?1-shifted:shifted)*W, defFloor, defCeil);
        ty=p.y*H;
      }
      glide(cur,tx,ty,DRIFT_SPEED);
    }
  });
}


// ── REPULSION — prevent teammates bunching into clumps ────────────────────
function applyRepulsion(){
  const REPEL_DIST=W*.028;
  const REPEL_FORCE=0.15;
  ['h','a'].forEach(side=>{
    const keys=Object.keys(sq(side)).filter(k=>sq(side)[k]&&PP[side][k]);
    for(let i=0;i<keys.length;i++){
      for(let j=i+1;j<keys.length;j++){
        const a=PP[side][keys[i]],b=PP[side][keys[j]];
        const dx=b.x-a.x,dy=b.y-a.y;
        const d=Math.hypot(dx,dy)||0.1;
        if(d<REPEL_DIST){
          const force=(REPEL_DIST-d)/REPEL_DIST*REPEL_FORCE;
          const nx=dx/d,ny=dy/d;
          if(keys[i]!==G.ck){a.x-=nx*force;a.y-=ny*force;}
          if(keys[j]!==G.ck){b.x+=nx*force;b.y+=ny*force;}
        }
      }
    }
  });
}

function pickCarrierAfterWin(side,winnerKey){
  const q=sq(side);
  if(winnerKey&&q[winnerKey]&&!ocd(side,winnerKey))return winnerKey;
  const ks=validOutfieldKeys(side).filter(k=>!ocd(side,k));
  if(!ks.length)return Object.keys(q).find(k=>q[k]&&k!=='GK')||'CM2';
  let best=ks[0],bestScore=-Infinity;
  ks.forEach(k=>{
    const p=PP[side][k];if(!p)return;
    const score=progressFor(side,p)*3 + nearestDefenderDistance(side,p)/(W*.15) + (zo(k)==='mid'?0.5:zo(k)==='att'?1:0);
    if(score>bestScore){bestScore=score;best=k;}
  });
  return best;
}

